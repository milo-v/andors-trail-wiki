const fs = require('fs')

const {XMLParser} = require('./xmlParser.js');

// Updates (rather than overwrites) the target .env file, so this can run
// once per dataset - stable's REACT_APP_AT_VERSION, dev's always-current
// REACT_APP_AT_VERSION_DEV - without either call clobbering the other's line.
function saveVersion(versionName, envVar, envPath) {
    const existing = fs.existsSync(envPath) ? fs.readFileSync(envPath, 'utf8') : '';
    const lines = existing.split('\n').filter((line) => line && !line.startsWith(envVar + '='));
    lines.push(envVar + '=' + versionName);
    fs.writeFileSync(envPath, lines.join('\n') + '\n');
}

function getXmlData(fileName, thenDo) {
    fs.readFile(fileName, 'utf8', (err, data) => {
        if (!data) {
            console.warn("'" + fileName + "' doesn't exist");
            return;
        }
        var str = data.replace(/<!--.*-->/g,'');
        thenDo(str);
    })
}

const getXml = (manifestPath, envVar, envPath) => {
    const thenDo = (xmlString) => {
        var parser = new XMLParser();
        var myXml = parser.parseFromString(xmlString);
        saveVersion(myXml.attributes["android:versionName"], envVar, envPath);
    }
    getXmlData(manifestPath, thenDo);
}

// Args: [manifestPath] [envVar] [envPath] - all optional, defaulting to the
// original single-dataset (stable) behavior so existing local-dev usage
// (`node bin/getVersion.js`) is unchanged. The dev dataset is generated by
// passing its own manifest path and REACT_APP_AT_VERSION_DEV explicitly (see
// deploy.yml).
const [, , manifestArg, envVarArg, envPathArg] = process.argv;
const manifestPath = manifestArg || "../andors-trail/AndorsTrail/app/src/main/AndroidManifest.xml";
const envVar = envVarArg || "REACT_APP_AT_VERSION";
const envPath = envPathArg || "./.env";

getXml(manifestPath, envVar, envPath);
